function buildTileMeta(minElev: number, maxElev: number): TileMetadata {
  const r = lastTileResult!;
  return {
    seed:               getParams().seed,
    params:             getParams() as unknown as Record<string, unknown>,
    lat:                r.lat,
    lon:                r.lon,
    terrain_class:      r.sampled_fields.terrain_class,
    sampled_fields:     r.sampled_fields as unknown as Record<string, unknown>,
    metrics:            r.score.metrics,
    score_total:        r.score.total,
    width:              r.width,
    height:             r.height,
    min_elevation:      minElev,
    max_elevation:      maxElev,
    generation_time_ms: r.generation_time_ms,
    timestamp:          new Date().toISOString(),
  };
}

