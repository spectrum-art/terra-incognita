// ── Tile-worker render ─────────────────────────────────────────────────────────

function renderResult(result: PlanetResult): void {
  const { width, height } = result;
  const data = new Float32Array(result.heights);
  const regs = new Uint8Array(result.regimes);

  // Compute min/max once.
  let min = Infinity, max = -Infinity;
  for (const v of data) { if (v < min) min = v; if (v > max) max = v; }

  canvas.width  = width;
  canvas.height = height;
  const ctx = canvas.getContext("2d")!;

  // Four quadrants: top-left, top-right, bottom-left, bottom-right.
  const midX = Math.floor(width  / 2);
  const midY = Math.floor(height / 2);
  const tiles = [
    [0, 0, midX, midY],
    [midX, 0, width, midY],
    [0, midY, midX, height],
    [midX, midY, width, height],
  ] as const;

  progressCont.style.display = "block";
  let done = 0;

  const heightsBuf = data.buffer.slice(0) as ArrayBuffer;
  const regsBuf    = regs.buffer.slice(0) as ArrayBuffer;

  tiles.forEach(([x0, y0, x1, y1]) => {
    const worker = new Worker(new URL("./workers/tile_worker.ts", import.meta.url), { type: "module" });
    worker.postMessage(
      { heights: heightsBuf.slice(0), regimes: regsBuf.slice(0),
        fullWidth: width, fullHeight: height,
        x0, y0, x1, y1, mode: renderMode, min, max },
      [heightsBuf.slice(0), regsBuf.slice(0)],
    );
    worker.onmessage = (e: MessageEvent) => {
      const { pixels, x0: tx0, y0: ty0, x1: tx1, y1: ty1 } = e.data as {
        pixels: ArrayBuffer; x0: number; y0: number; x1: number; y1: number;
      };
      const tileW = tx1 - tx0;
      const tileH = ty1 - ty0;
      const imgData = new ImageData(new Uint8ClampedArray(pixels), tileW, tileH);
      ctx.putImageData(imgData, tx0, ty0);
      done++;
      progressBar.style.width = `${(done / 4) * 100}%`;
      if (done === 4) {
        progressCont.style.display = "none";
        progressBar.style.width = "0%";
      }
      worker.terminate();
    };
  });
}

// ── Generate button — Phase A: planet overview ─────────────────────────────────

async function runGenerate(): Promise<void> {
  const params: GlobalParams = getParams();
  generateBtn.disabled = true;
  statusEl.textContent = "Generating planet overview…";
  // Auto-close tile detail panel and reset its position when generating a new planet.
  tileDetailPanel.classList.remove("visible");
  panelTx = 0;
  panelTy = 0;
  tileDetailPanel.style.transform = "";

  try {
    // generate_overview() is synchronous in WASM.
    const overview = generate_overview(params) as PlanetOverviewResult;
    lastOverview = overview;

    // Render planet overview via planet_renderer.
    renderPlanetOverview(canvas, overview);

    // Sync globe texture with freshly rendered overview canvas.
    if (globeRenderer) globeRenderer.updateTexture(canvas);

    // Show planet metrics panel (left overlay).
    renderPlanetMetricsPanel(planetMetricsPanel, overview.planet_metrics, overview.generation_time_ms);

    exportDiv.classList.add("visible");
    statusEl.textContent = `Done — ${overview.generation_time_ms} ms`;
  } catch (err) {
    statusEl.textContent = `Error: ${err}`;
    console.error(err);
  } finally {
    generateBtn.disabled = false;
  }
}

// ── Optional tile generation (preserves Phase 7/8 pipeline for Phase B) ────────

async function runTileGenerate(): Promise<void> {
  const params: GlobalParams = getParams();
  generateBtn.disabled = true;
  statusEl.textContent = "Generating tile…";

  try {
    const result = generate(params) as PlanetResult;
    lastResult = result;
    renderResult(result);
    renderScorePanel(scorePanel, result.score, result.generation_time_ms);
    exportDiv.classList.add("visible");
    statusEl.textContent = `Tile done — ${result.generation_time_ms} ms`;
  } catch (err) {
    statusEl.textContent = `Error: ${err}`;
    console.error(err);
  } finally {
    generateBtn.disabled = false;
  }
}

// Expose tile generation for console use (Phase B drill-down foundation).
(window as unknown as Record<string, unknown>)["runTileGenerate"] = runTileGenerate;

// ── Globe toggle (PB.1) ────────────────────────────────────────────────────────

viewToggleBtn.addEventListener("click", () => {
  isGlobeView = !isGlobeView;
  if (isGlobeView) {
    canvas.style.display = "none";
    globeContainer.style.display = "block";
    viewToggleBtn.textContent = "Flat View";
    // Sync globe texture with current canvas state, then show the WebGL canvas
    if (globeRenderer) {
      if (lastOverview) globeRenderer.updateTexture(canvas);
      globeRenderer.show();
    }
  } else {
    if (globeRenderer) globeRenderer.hide();
    canvas.style.display = "";
    globeContainer.style.display = "none";
    viewToggleBtn.textContent = "Globe View";
  }
});

// ── Generate-at-location button (PB.3) ─────────────────────────────────────────

genLocationBtn.addEventListener("click", () => { void runGenerateAtLocation(); });

async function runGenerateAtLocation(): Promise<void> {
  const pos = interactionMgr?.selection;
  if (!pos) return;

  const params = getParams();
  genLocationBtn.disabled = true;
  statusEl.textContent = `Generating tile at ${pos.lat.toFixed(1)}°, ${pos.lon.toFixed(1)}°…`;

  try {
    const result = generate_at_location_wasm(params, pos.lat, pos.lon) as LocationTileResult;
    lastTileResult = result;

    renderDetailPanel(
      tileDetailPanel, tileCanvasCont, tileInfoEl, tileConsistEl,
      result, canvas,
    );
    statusEl.textContent =
      `Tile done — ${result.generation_time_ms} ms — ` +
      `${result.sampled_fields.terrain_class} at ${pos.lat.toFixed(1)}°, ${pos.lon.toFixed(1)}°`;
  } catch (err) {
    statusEl.textContent = `Error: ${err}`;
    console.error(err);
  } finally {
    genLocationBtn.disabled = false;
  }
}

